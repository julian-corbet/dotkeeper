func initCmd() *cobra.Command {
	var name string
	var slot int
	var force bool
	var noService bool

	cmd := &cobra.Command{
		Use:   "init",
		Short: "Initialize this machine for dotkeeper",
		Long:  "Creates machine identity (machine.toml v2), generates Syncthing keys, and installs services.\nRun this on the first machine.",
		Run: func(cmd *cobra.Command, args []string) {
			existing, _ := config.LoadMachineConfigV2()

			if name == "" {
				hostname, _ := os.Hostname()
				name = hostname
			}
			if slot < 0 {
				slot = 0
			}

			// 1. Machine identity (v2 schema). If machine.toml already exists
			// (for example generated by Home Manager), preserve it and only
			// initialise missing runtime state below.
			if existing == nil || force {
				mcfg := &config.MachineConfigV2{
					SchemaVersion:            2,
					Name:                     name,
					Slot:                     uint(slot),
					DefaultCommitPolicy:      "manual",
					DefaultGitInterval:       "daily",
					DefaultSlotOffsetMinutes: 5,
					ReconcileInterval:        "5m",
					DefaultShareWith:         []string{},
					Peers:                    []config.PeerEntry{},
					Discovery: config.DiscoveryConfig{
						ScanRoots:    []string{"~/Documents/GitHub"},
						Exclude:      []string{},
						ScanInterval: "5m",
						ScanDepth:    3,
					},
				}
				if err := config.WriteMachineConfigV2(mcfg); err != nil {
					fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: %v\n", err)
					os.Exit(1)
				}
				fmt.Printf("[dotkeeper] machine: %s (slot %d)\n", name, slot)
			} else {
				fmt.Printf("[dotkeeper] already initialized as '%s' (slot %d); preserving machine.toml\n", existing.Name, existing.Slot)
			}

			// 2. Syncthing identity
			eng := engine()
			if err := eng.Setup(); err != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: %v\n", err)
				os.Exit(1)
			}
			deviceID, err := eng.DeviceID()
			if err != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: %v\n", err)
				os.Exit(1)
			}
			fmt.Printf("[dotkeeper] Syncthing identity generated\n")
			if err := ensureStateDeviceID(deviceID); err != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: %v\n", err)
				os.Exit(1)
			}
			fmt.Printf("[dotkeeper] state.toml ready\n")

			// 3. Syncthing service
			if noService {
				fmt.Printf("[dotkeeper] service install skipped (--no-service)\n")
			} else {
				mgr := svcManager()
				binPath := binaryPath()
				if err := mgr.InstallSyncthing(binPath); err != nil {
					fmt.Fprintf(os.Stderr, "[dotkeeper] WARNING: service install: %v\n", err)
				} else {
					fmt.Printf("[dotkeeper] started Syncthing service (%s)\n", service.PlatformName(mgr))
				}
			}

			// 4. Print result
			fmt.Println()
			fmt.Printf("[dotkeeper] device ID: %s\n", deviceID)
			fmt.Println()
			fmt.Println("[dotkeeper] to add repos, run: dotkeeper track <path>")
			fmt.Println("[dotkeeper] or place a local .dotkeeper.toml in any repo under a scan root")
			fmt.Println("[dotkeeper] then run: dotkeeper reconcile")
		},
	}
	cmd.Flags().StringVar(&name, "name", "", "machine name (default: hostname)")
	cmd.Flags().IntVar(&slot, "slot", -1, "timer slot (default: 0)")
	cmd.Flags().BoolVar(&force, "force", false, "reinitialize")
	cmd.Flags().BoolVar(&noService, "no-service", false, "initialize config/state but do not install or start a service")
	return cmd
}

func ensureStateDeviceID(deviceID string) error {
	return config.MutateStateV2(func(state *config.StateV2) error {
		state.SyncthingDeviceID = deviceID
		return nil
	})
}

// statusCmd shows the machine status: machine name, slot, scan roots,
// peer count from state.toml, tracked override count, and Syncthing connection.
func statusCmd() *cobra.Command {
	return &cobra.Command{
		Use:   "status",
		Short: "Show full status",
		RunE: func(cmd *cobra.Command, args []string) error {
			machine, machineErr := config.LoadMachineConfigV2()
			// A non-nil error means the file exists but failed to parse — tell the
			// user rather than silently treating it as "not initialised".
			if machineErr != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: machine.toml parse failure: %v\n", machineErr)
				return fmt.Errorf("machine.toml parse failure: %w", machineErr)
			}

			state, stateErr := config.LoadStateV2()
			if stateErr != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: state.toml parse failure: %v\n", stateErr)
				return fmt.Errorf("state.toml parse failure: %w", stateErr)
			}

			fmt.Println("=== Machine ===")
			if machine != nil {
				fmt.Printf("  Name: %s\n", machine.Name)
				fmt.Printf("  Slot: %d\n", machine.Slot)
				fmt.Printf("  Scan roots: %s\n", strings.Join(machine.Discovery.ScanRoots, ", "))
			} else {
				fmt.Println("  Not initialized (run 'dotkeeper init')")
			}

			fmt.Printf("\n=== Syncthing (embedded, API %s) ===\n", stclient.APIAddress)
			eng := engine()
			deviceID, _ := eng.DeviceID()

			mgr := svcManager()
			if mgr.IsSyncthingRunning() {
				fmt.Println("  Status: running")
				if deviceID != "" {
					fmt.Printf("  Device ID: %s\n", deviceID)
				}
				fmt.Printf("  Sync port: 12000\n")

				client := apiClient()
				if apiCfg, err := client.GetConfig(); err == nil {
					devices, _ := apiCfg["devices"].([]any)
					folders, _ := apiCfg["folders"].([]any)
					peerCount := 0
					for _, d := range devices {
						dm, _ := d.(map[string]any)
						did, _ := dm["deviceID"].(string)
						if did != deviceID {
							peerCount++
							dname, _ := dm["name"].(string)
							fmt.Printf("  Peer: %s (%s)\n", dname, truncateID(did))
						}
					}
					if peerCount == 0 {
						fmt.Println("  Peers: none")
					}
					fmt.Printf("  Folders: %d\n", len(folders))
				}
			} else {
				fmt.Println("  Status: not running")
				if deviceID != "" {
					fmt.Printf("  Device ID: %s\n", deviceID)
				}
			}

			if state != nil {
				peers := mergedPeersForDisplay()
				if len(peers) > 0 {
					fmt.Printf("\n=== Peers (%d) ===\n", len(peers))
					for _, p := range peers {
						fmt.Printf("  %s: %s (%s)\n", p.Name, truncateID(p.DeviceID), p.Source)
					}
				} else {
					fmt.Println("\n=== Peers ===")
					fmt.Println("  none (pair machines with: dotkeeper reconcile)")
				}

				if len(state.TrackedOverrides) > 0 {
					fmt.Printf("\n=== Tracked overrides (%d) ===\n", len(state.TrackedOverrides))
					for _, p := range state.TrackedOverrides {
						fmt.Printf("  %s\n", config.ContractPath(p))
					}
				}

				if len(state.ObservedRepos) > 0 {
					fmt.Printf("\n=== Observed repos (%d) ===\n", len(state.ObservedRepos))
					for path, obs := range state.ObservedRepos {
						lastReconcile := "never"
						if !obs.LastBackupAt.IsZero() {
							lastReconcile = obs.LastBackupAt.Format("2006-01-02 15:04:05")
						}
						fmt.Printf("  %s (last backup: %s)\n", config.ContractPath(path), lastReconcile)
					}
				}
			}
			return nil
		},
	}
}

func startCmd() *cobra.Command {
	var debug bool

	cmd := &cobra.Command{
		Use:   "start",
		Short: "Start embedded Syncthing + reconcile daemon (foreground, for systemd)",
		Long: "Starts the embedded Syncthing engine, the conflict watcher, and the\n" +
			"reconcile daemon. The reconciler runs on a periodic timer and reacts to\n" +
			"filesystem changes in scan roots, state.toml, and machine.toml.\n\n" +
			"Use --debug to raise the log level to DEBUG.",
		Run: func(cmd *cobra.Command, args []string) {
			// SIGINT/SIGTERM is wired into the root context in main(), so
			// cmd.Context() already cancels on signal. No need to redo it.
			ctx := cmd.Context()

			// Self-apply nice=19 / ioprio=idle before any heavy work
			// starts. The packaged systemd user unit already sets these
			// (Nice=19, IOSchedulingClass=idle, CPUWeight=10) — on those
			// installs LowerSelf is an idempotent no-op. The point is
			// installs that bypass the unit: Docker containers built by
			// third parties, manual `dotkeeper start` in a dev loop,
			// distro packagers who shipped a stripped-down unit. The
			// embedded Syncthing scanner is heavy enough that running
			// without de-prioritisation is user-visible on weaker
			// hardware. Non-Linux platforms compile to a no-op.
			procnice.LowerSelf()

			// Configure slog handler. Writes to os.Stdout because
			// engine.Start later dup2's fd 1 to ~/.local/state/dotkeeper/
			// syncthing.log, and we want our slog output to land in the
			// same file as Syncthing's. Using os.Stderr would route to
			// fd 2 (the systemd journal socket), bypassing the file.
			// This matters more on v0.8+ because Syncthing v2 also uses
			// log/slog (via the default handler), so anything we install
			// here intercepts Syncthing's output too.
			logLevel := slog.LevelInfo
			if debug {
				logLevel = slog.LevelDebug
			}
			logger := slog.New(slog.NewTextHandler(os.Stdout, &slog.HandlerOptions{
				Level: logLevel,
			}))
			slog.SetDefault(logger)

			// Spin up the conflict watcher alongside Syncthing so we
			// log sync-conflict files as soon as they appear. This is a
			// best-effort: if no config is present (first boot) or no
			// managed folders exist yet, we just skip it.
			stopWatcher := startConflictWatcher(ctx)
			defer stopWatcher()

			// Spin up the activity tracker for auto-pause. Independent
			// of the conflict watcher because its purpose is different
			// (per-folder timestamps vs sync-conflict-file detection)
			// and we want activity tracking to keep running even if
			// the conflict watcher fails. Best-effort: if no managed
			// folders exist yet, returns nil tracker and auto-pause is
			// effectively disabled until next reconcile rediscovers.
			tracker := startActivityTracker(ctx, logger)
			defer func() {
				if tracker != nil {
					_ = tracker.Close()
				}
			}()

			// Build the transport manager: pre-classifies every
			// transport's availability, runs initial discovery
			// against every paired peer, builds the per-(transport,
			// peer) cost model graph. Subsequent reconcile cycles
			// consult the manager via Manager.Route to pick the
			// optimal transport per change. The variable currently
			// holds the manager for future use by reconcile and the
			// CLI; this v1.0.0 increment lays the wiring without
			// yet swapping reconcile's stclient-direct propagation
			// for manager-driven routing — that swap lands the
			// observation-feedback loop and the operator-visible
			// behaviour change in one cohesive PR.
			transportMgr := startTransports(ctx, logger)
			propagator := buildPropagator(transportMgr, logger)

			// Spin up the smart-rescan support: watchhealth tracker
			// (per-folder filesystem classification + overflow flags),
			// sleep/wake detector (heartbeat-based, no D-Bus/Cocoa
			// dependency), and the rescan-timestamp recorder used by
			// the backstop check. All three are cheap to leave running;
			// if reconcile decides nothing's drifted, no rescans fire.
			health := startWatchHealth(ctx, logger)
			healthAd := newHealthAdapter(health)
			wakeFlag := startWakeDetector(ctx, logger)
			rescanLog := newRescanLog()
			// Seed the rescan log so the first reconcile after
			// daemon start does not fire a backstop RescanFolderNow
			// for every folder. See seed() comment for rationale.
			rescanLog.seed(managedFolderPathsV5(), time.Now())

			// Forward activity events into the watchhealth tracker so
			// LastReliableEventAt advances. Tracker already drives the
			// auto-pause loop; this just teaches it to also feed
			// watchhealth's liveness clock.
			if tracker != nil && health != nil {
				go forwardActivityToHealth(ctx, tracker, health)
			}

			// Active transport benchmarker: periodically measures
			// synchronous transports per (peer, folder) tuple so the
			// cost model self-tunes even for folders that rarely see
			// organic edits. Skips itself when the local model has
			// converged or when recent organic traffic already
			// provides a fresh observation. Skips Syncthing (async,
			// no inline timing possible) — that transport's params
			// stay at the prior. No-op when no transport manager is
			// available (the build below also no-ops on the same
			// condition).
			startBenchmarker(ctx, transportMgr, logger)

			// Start the reconcile daemon alongside Syncthing.
			startReconcileDaemon(ctx, logger, tracker, healthAd, wakeFlag, rescanLog, propagator)

			eng := engine()
			if err := eng.Start(ctx); err != nil {
				fmt.Fprintf(os.Stderr, "[dotkeeper] ERROR: %v\n", err)
				os.Exit(1)
			}
		},
	}
	cmd.Flags().BoolVar(&debug, "debug", false, "enable debug-level logging")
	return cmd
}

// startConflictWatcher starts a conflict.Watcher over every managed
// folder discovered from current state. Returns a cleanup function that
// closes the watcher. Failures are logged, never fatal — Syncthing must
// keep running even if the watcher can't start.
func startConflictWatcher(ctx context.Context) func() {
	roots := managedFolderPathsV5()
	if len(roots) == 0 {
		return func() {}
	}

	w, err := conflict.New(roots)
	if err != nil {
		fmt.Fprintf(os.Stderr, "[dotkeeper] WARNING: conflict watcher: %v\n", err)
		return func() {}
	}

	// Load auto-resolve setting: default true, controlled by machine config
	// in a future version. For now always enabled.
	autoResolve := true

	done := make(chan struct{})
	go func() {
		defer close(done)
		for {
			select {
			case <-ctx.Done():
				return
			case c, ok := <-w.Events():
				if !ok {
					return
				}
				handleConflictEvent(ctx, c, roots, autoResolve)
			case err, ok := <-w.Errors():
				if !ok {
					return
				}
				fmt.Fprintf(os.Stderr, "[dotkeeper] WARNING: conflict watcher: %v\n", err)
			}
		}
	}()

	return func() {
		_ = w.Close()
		<-done
	}
}

// handleConflictEvent runs the resolver chain on one detected conflict
// and emits exactly one log line summarising the outcome. Errors from
// individual resolvers become "kept (<reason>)" log lines — they never
// escape to abort the watcher loop.
func handleConflictEvent(ctx context.Context, c conflict.Conflict, roots []string, autoResolve bool) {
	when := c.Timestamp.Format("2006-01-02 15:04:05")

	// Phase 1 behaviour (detection-only) when the user opted out.
	if !autoResolve {
		fmt.Printf("[dotkeeper] kept: %s (from device %s at %s; auto-resolve disabled)\n",
			c.Path, c.DeviceIDShort, when)
		return
	}

	// Try dedup first — cheapest, safest, and covers the commonest
	// "same save on two machines" case without touching git at all.
	if action, err := conflict.ResolveIdentical(c); err == nil && action == conflict.ActionDeduped {
		fmt.Printf("[dotkeeper] deduped: %s (from device %s at %s)\n",
			c.Path, c.DeviceIDShort, when)
		return
	} else if err != nil {
		fmt.Fprintf(os.Stderr, "[dotkeeper] WARNING: dedup check %s: %v\n", c.Path, err)
		// Fall through — maybe the merge path still works.
	}

	// Then the 3-way merge. repoRoot is the managed-folder root the
	// conflict lives under, which must also be a git repo for the
	// merge to land; non-repo folders fail gracefully below.
	repoRoot := containingFolder(c.Path, roots)
	action, err := conflict.ResolveTextMerge(ctx, c, repoRoot)
	switch {
	case err != nil:
		fmt.Fprintf(os.Stderr, "[dotkeeper] WARNING: merge %s: %v\n", c.Path, err)
		fmt.Printf("[dotkeeper] kept: %s (from device %s at %s; merge error)\n",
			c.Path, c.DeviceIDShort, when)
	case action == conflict.ActionMerged:
		fmt.Printf("[dotkeeper] merged: %s (from device %s at %s)\n",
			c.Path, c.DeviceIDShort, when)
	default:
		fmt.Printf("[dotkeeper] kept: %s (from device %s at %s; manual resolution required)\n",
			c.Path, c.DeviceIDShort, when)
	}
}

// managedFolderPathsV5 returns absolute, existing paths for every managed
// folder discovered from current state: config dir + TrackedOverrides + repos
// found by walking scan roots for .dotkeeper.toml files.
//
// ObservedRepos is intentionally NOT consulted: if a scan_root is removed
// from machine.toml, repos under it should become invisible to the watcher,
// and the next reconcile will prune them from ObservedRepos.
//
// Missing paths are skipped silently. The actual discovery logic lives in
// internal/discovery so both this function and the doctor package share one
// canonical implementation.
func managedFolderPathsV5() []string {
	return discovery.ManagedFolderPaths()
}

