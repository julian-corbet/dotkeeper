// startTransports constructs every transport implementation that
// dotkeeper supports, wires them into a Manager, and triggers
// initial discovery for every known peer. Returns the Manager so
// reconcile and the CLI can consult it.
//
// Transport ordering matters: Manager.Route uses list order as
// tie-breaker when two transports predict equal costs, and
// Syncthing is intentionally listed LAST so other transports win
// any tie. Rationale: Syncthing is the universal fallback that
// always works; "use the alternative when it's at least as good"
// is the desired bias.
//
// Discovery runs once at startup. Subsequent refresh happens only
// on explicit triggers (wake from suspend → reconcile loop calls
// Manager.InvalidatePeer for each peer, forcing rediscovery on
// next access; an explicit `dotkeeper transport rediscover` CLI
// verb is a planned extension). No periodic background probing —
// peer connectivity is topology, not weather.
func startTransports(ctx context.Context, logger *slog.Logger) *transport.Manager {
	transports := buildTransportList()

	names := make([]string, 0, len(transports))
	for _, t := range transports {
		names = append(names, t.Name()+"("+availabilityString(t.Available())+")")
	}
	logger.InfoContext(ctx, "transports configured", "list", strings.Join(names, ","))

	mgr := transport.NewManager(transports)

	// Initial discovery for each configured peer. Pulled from
	// machine.toml directly because the desired-state provider's
	// peer list is the authoritative source and we want it
	// available before reconcile runs its first cycle.
	if machine, err := config.LoadMachineConfigV2(); err == nil && machine != nil {
		for _, p := range machine.Peers {
			peer := transport.Peer{
				Name:     p.Name,
				DeviceID: p.DeviceID,
				Hostname: p.Name, // v1.0 defaults Hostname to Name; v1.1 will let users override
			}
			mgr.Discover(ctx, peer)
			// Log which transports were found reachable. The
			// "best for this size" choice happens per-change via
			// Manager.Route; at startup we just report topology.
			routes, _ := mgr.RoutesFor(peer.Name)
			var reachable []string
			for name, entry := range routes.Entries {
				if entry.Reachable {
					reachable = append(reachable, name)
				}
			}
			logger.InfoContext(ctx, "peer discovery completed",
				"peer", peer.Name,
				"reachable", strings.Join(reachable, ","))
		}
	}
	return mgr
}

// buildTransportList constructs every transport implementation
// dotkeeper supports on this build. Order is load-bearing — see
// startTransports for the tie-break rationale.
//
// Auto-detection happens here: any transport whose prerequisites
// can be checked without blocking is queried; transports that
// require remote calls to determine availability defer to their
// own Available() at runtime.
func buildTransportList() []transport.Transport {
	var transports []transport.Transport

	// GitSSH variants are registered when their resolver's
	// prerequisites are satisfied on this host. v1.0.0 ships one
	// resolver (Tailscale); the Resolver interface is the seam for
	// mDNS and static-hub variants in follow-up releases. The
	// order of resolvers here is the order the Manager consults
	// when ranking transports for a peer; the cost model decides
	// per-change which one is actually used.
	ts := transport.NewTailscaleResolver()
	if ts.Available() {
		transports = append(transports, transport.NewGitSSHTransport(ts))
		// Mutagen runs over the same SSH path as GitSSH; if the
		// resolver is good, Mutagen tries to claim itself too. The
		// transport's own Available() guards against `mutagen`
		// missing on PATH, so this entry is a no-op on hosts
		// without it.
		mtg := transport.NewMutagenTransport(ts)
		if mtg.Available() {
			transports = append(transports, mtg)
		}
	}

	// Syncthing always last. It's the universal fallback — every
	// dotkeeper install has it built in, so it's never absent.
	if key, err := engine().APIKey(); err == nil {
		transports = append(transports, transport.NewSyncthingTransport(stclient.New(key)))
	}

	return transports
}

func availabilityString(b bool) string {
	if b {
		return "available"
	}
	return "unavailable"
}

// forwardActivityToHealth subscribes to the activity tracker's
// Hints channel and forwards each event into the watchhealth
// tracker as MarkEventDelivered. This keeps LastReliableEventAt
// fresh on every watched folder so the "watcher has been silent
// for too long" liveness check (planned for v0.9.8) has accurate
// data. v0.9.7 doesn't yet branch on this field but populating it
// avoids a "missing data" issue when the check is added later.
func forwardActivityToHealth(ctx context.Context, tracker *activity.Tracker, health *watchhealth.Tracker) {
	for {
		select {
		case <-ctx.Done():
			return
		case root, ok := <-tracker.Hints():
			if !ok {
				return
			}
			health.MarkEventDelivered(root, time.Now())
		}
	}
}

// startWatchHealth instantiates the watchhealth.Tracker and
// pre-classifies every currently-managed folder. Returns the
// tracker (typed pointer rather than interface) so callers can
// supply it both as a HealthQuerier to the provider and as a
// HealthResetter to the applier.
//
// Reclassification on the fly (folders added after startup) is
// deferred to v0.9.8 — same trade-off as the activity tracker:
// folders added post-startup don't get smart rescan until next
// service restart, which is recoverable.
func startWatchHealth(ctx context.Context, logger *slog.Logger) *watchhealth.Tracker {
	wh := watchhealth.New()
	for _, root := range managedFolderPathsV5() {
		st := wh.Register(root)
		logger.InfoContext(ctx, "watchhealth classified folder",
			"path", root,
			"fs", st.FilesystemType,
			"kind", st.Kind.String())
	}
	return wh
}

// startWakeDetector spawns the heartbeat-based suspend/resume
// detector and returns a WakeFlag adapter that the provider can
// Take() once per cycle. The detector runs at 30s ticks and emits
// when wall-clock advances by >60s between ticks.
func startWakeDetector(ctx context.Context, logger *slog.Logger) reconcileWakeFlag {
	events := watchhealth.StartSleepDetector(ctx, 30*time.Second)
	wf := &wakeFlag{}
	go func() {
		for ev := range events {
			logger.InfoContext(ctx, "suspend/resume detected", "gap", ev.Gap.String())
			wf.set()
		}
	}()
	return wf
}

// wakeFlag is a one-shot bool consumed by reconcile via Take().
// The detector goroutine sets it; the provider consumes it once
// per cycle.
type wakeFlag struct {
	mu  sync.Mutex
	hot bool
}

func (w *wakeFlag) set() {
	w.mu.Lock()
	w.hot = true
	w.mu.Unlock()
}

func (w *wakeFlag) Take() bool {
	w.mu.Lock()
	defer w.mu.Unlock()
	v := w.hot
	w.hot = false
	return v
}

// reconcileWakeFlag is a local interface alias matching reconcile's
// WakeFlag so the public API stays narrow.
type reconcileWakeFlag interface {
	Take() bool
}

// buildPropagator constructs the v1.0.0 daemon-side
// CommitPropagator. Returns nil when no transport manager exists
// (defensive — reconcile degrades to "no peer propagation," same
// as releases before v1.0.0).
//
// Both the peer roster and the managed-folder set are queried
// FRESH on every PropagateNewCommit call. machine.toml is small
// and managedFolderPathsV5 walks the same in-process state that
// reconcile updates, so the per-commit cost is dominated by the
// existing push work, not the lookups. The freshness matters
// because dotkeeper users edit machine.toml or run `dotkeeper
// track` without restarting the daemon and reasonably expect the
// next commit to find the change.
func buildPropagator(mgr *transport.Manager, logger *slog.Logger) reconcile.CommitPropagator {
	if mgr == nil {
		return nil
	}
	return newDaemonPropagatorWithSources(mgr, livePeersSource(), livePropagatorFoldersSource(), logger)
}

// livePeersSource returns a closure that re-reads machine.toml on
// every invocation. Shared between the propagator and the active
// benchmarker so both surfaces stay in sync with operator config
// edits without a daemon restart.
func livePeersSource() func() []transport.Peer {
	return func() []transport.Peer {
		machine, err := config.LoadMachineConfigV2()
		if err != nil || machine == nil {
			return nil
		}
		peers := make([]transport.Peer, 0, len(machine.Peers))
		for _, p := range machine.Peers {
			peers = append(peers, transport.Peer{
				Name:     p.Name,
				DeviceID: p.DeviceID,
				Hostname: p.Name,
			})
		}
		return peers
	}
}

// livePropagatorFoldersSource returns a closure shaped for the
// propagator (path-keyed map). Re-walks managedFolderPathsV5 on
// each call so freshly-added folders propagate without restart.
func livePropagatorFoldersSource() func() map[string]transport.Folder {
	return func() map[string]transport.Folder {
		roots := managedFolderPathsV5()
		byPath := make(map[string]transport.Folder, len(roots))
		for _, root := range roots {
			byPath[root] = transport.Folder{
				ID:   filepath.Base(root),
				Path: root,
			}
		}
		return byPath
	}
}

// liveBenchmarkerFoldersSource returns a closure shaped for the
// benchmarker (flat slice). Same freshness contract as the
// propagator's source.
func liveBenchmarkerFoldersSource() func() []transport.Folder {
	return func() []transport.Folder {
		roots := managedFolderPathsV5()
		out := make([]transport.Folder, 0, len(roots))
		for _, root := range roots {
			out = append(out, transport.Folder{
				ID:   filepath.Base(root),
				Path: root,
			})
		}
		return out
	}
}

// startBenchmarker spins up the active-transport benchmarker if
// the transport manager is available. No-op otherwise — without a
// manager there's nothing to benchmark and the daemon degrades to
// pre-v1.1.22 behaviour (cost model learns only from organic
// observations).
func startBenchmarker(ctx context.Context, mgr *transport.Manager, logger *slog.Logger) {
	if mgr == nil {
		return
	}
	b := benchmarker.New(mgr, liveBenchmarkerFoldersSource(), livePeersSource(), logger, benchmarker.Options{})
	go b.Run(ctx)
}

// daemonPropagator implements reconcile.CommitPropagator on top of
// the v1.0.0 transport.Manager. For each successful local commit,
// it fans out to every paired peer, asks the Manager for the
// optimal transport given the change's measured size, executes
// the push via Transport.PropagateChange, and feeds the observed
// elapsed time back into the Manager's cost model.
//
// All work is best-effort: a per-peer push failure logs but does
// not propagate to the caller, because the local commit is
// canonical state and peers eventually catch up via Syncthing's
// universal fallback or via subsequent reconcile cycles.
//
// Freshness: the peer roster and folder map are queried via the
// peersSource / foldersSource callbacks on every PropagateNewCommit
// invocation. This means peers added to machine.toml or folders
// added via `dotkeeper track` after daemon startup are picked up on
// the very next commit cycle, rather than only after a daemon
// restart. The previous design captured both at construction and
// silently skipped late-added peers/folders — a real source of
// "I added the peer, why isn't it receiving pushes?" friction.
type daemonPropagator struct {
	mgr    *transport.Manager
	logger *slog.Logger

	// peersSource returns the current peer roster. Called on each
	// PropagateNewCommit; cost is one machine.toml read which is
	// noise on the post-commit hot path.
	peersSource func() []transport.Peer

	// foldersSource returns the current managed-folder map keyed by
	// absolute working-tree path. Same freshness contract as
	// peersSource.
	foldersSource func() map[string]transport.Folder
}

// newDaemonPropagator constructs a propagator with fixed peer and
// folder snapshots — the historical "captured at startup" semantics
// preserved for the tests that pass explicit slices. Production
// callers should use newDaemonPropagatorWithSources so peer/folder
// additions become visible on the next commit, not the next restart.
func newDaemonPropagator(mgr *transport.Manager, peers []transport.Peer, folders []transport.Folder, logger *slog.Logger) *daemonPropagator {
	byPath := make(map[string]transport.Folder, len(folders))
	for _, f := range folders {
		byPath[f.Path] = f
	}
	return newDaemonPropagatorWithSources(mgr,
		func() []transport.Peer { return peers },
		func() map[string]transport.Folder { return byPath },
		logger)
}

// newDaemonPropagatorWithSources is the source-based constructor:
// the propagator calls peersSource and foldersSource fresh on every
// PropagateNewCommit. nil callbacks are treated as "no peers / no
// folders" — propagator becomes a no-op, which is the right
// degenerate behaviour when machine state isn't loadable.
func newDaemonPropagatorWithSources(mgr *transport.Manager, peersSource func() []transport.Peer, foldersSource func() map[string]transport.Folder, logger *slog.Logger) *daemonPropagator {
	return &daemonPropagator{
		mgr:           mgr,
		logger:        logger,
		peersSource:   peersSource,
		foldersSource: foldersSource,
	}
}

func (p *daemonPropagator) PropagateNewCommit(ctx context.Context, folderPath string) {
	if p.mgr == nil {
		return
	}
	var peers []transport.Peer
	if p.peersSource != nil {
		peers = p.peersSource()
	}
	if len(peers) == 0 {
		return
	}
	var folders map[string]transport.Folder
	if p.foldersSource != nil {
		folders = p.foldersSource()
	}
	folder, ok := folders[folderPath]
	if !ok {
		p.logger.DebugContext(ctx, "propagator: no folder known for path", "path", folderPath)
		return
	}

	// Approximate change size from the last commit's diff. Used by
	// Manager.Route to pick between transports; the cost model
	// improves with every observation, so the initial estimate
	// doesn't need to be perfect.
	sizeBytes := estimateLastCommitSize(folderPath)

	change := transport.Change{
		Folder:   folder,
		SizeHint: sizeBytes,
		Kind:     transport.ChangeKindUnknown,
	}

	for _, peer := range peers {
		t, err := p.mgr.Route(change, peer.Name)
		if err != nil {
			p.logger.WarnContext(ctx, "propagator: no route to peer",
				"peer", peer.Name, "err", err)
			continue
		}
		start := time.Now()
		if err := t.PropagateChange(ctx, change, peer); err != nil {
			p.logger.WarnContext(ctx, "propagator: push failed",
				"peer", peer.Name, "transport", t.Name(), "err", err)
			continue
		}
		elapsed := time.Since(start)
		// Only synchronous transports produce a duration that
		// reflects real work. SyncthingTransport.PropagateChange
		// is a no-op (BEP gossip handles the actual propagation in
		// the background), so its ~µs elapsed would teach the cost
		// model that it's infinitely fast — defeating Manager.Route
		// for every subsequent decision.
		if t.PropagatesSynchronously() {
			p.mgr.RecordTransfer(t.Name(), peer.Name, folder.ID, sizeBytes, elapsed)
			p.logger.InfoContext(ctx, "propagator: pushed",
				"peer", peer.Name,
				"transport", t.Name(),
				"bytes", sizeBytes,
				"elapsed", elapsed.Round(time.Millisecond).String())
		} else {
			p.logger.InfoContext(ctx, "propagator: queued for async transport",
				"peer", peer.Name,
				"transport", t.Name(),
				"bytes", sizeBytes)
		}
	}
}

// estimateLastCommitSize approximates the byte count of the
// most-recent commit's changes by parsing `git diff --shortstat
// HEAD~1 HEAD`. Returns 0 (interpreted by Manager.Route as
// "unknown size; fall back to defaults") when the diff query
// fails — e.g. on the very first commit where HEAD~1 doesn't
// exist.
//
// The estimate is an upper bound — shortstat counts inserted +
// deleted line bytes but ignores binary deltas. For dotkeeper's
// text-heavy workload that's a fine approximation; the cost model
// learns the per-byte multiplier empirically anyway.
func estimateLastCommitSize(folderPath string) int64 {
	cmd := exec.Command("git", "diff", "--shortstat", "HEAD~1", "HEAD")
	cmd.Dir = folderPath
	out, err := cmd.Output()
	if err != nil {
		return 0
	}
	// shortstat format: " N files changed, X insertions(+), Y deletions(-)"
	// (singular forms "insertion(+)" / "deletion(-)" for X=1). We
	// extract the integer immediately preceding each "insertion" /
	// "deletion" token by walking backward through whitespace and
	// digits. A regex would be cleaner but pulls in regexp for a
	// one-off use; the walk-backward parser is ~10 lines and has
	// no other dependencies.
	s := string(out)
	inserts := numberBefore(s, "insertion")
	deletes := numberBefore(s, "deletion")
	// 50 bytes/line is a rough proxy for the cost model — the model's
	// per-byte coefficient is learned online, so any consistent
	// scaling factor works equivalently. We pick 50 because it's a
	// reasonable average source-line length and produces sizes in
	// the same order of magnitude as actual transfer payloads.
	return (inserts + deletes) * 50
}

// numberBefore returns the integer immediately preceding `marker`
// in `s`, or 0 when the marker is absent or no integer precedes it.
// Walks backward from the marker, skipping a single whitespace
// character (e.g. between "10" and "insertions"), then accumulates
// consecutive digits.
func numberBefore(s, marker string) int64 {
	idx := strings.Index(s, marker)
	if idx <= 0 {
		return 0
	}
	// Walk backward over whitespace.
	end := idx
	for end > 0 && (s[end-1] == ' ' || s[end-1] == '\t') {
		end--
	}
	// Walk backward over digits.
	start := end
	for start > 0 && s[start-1] >= '0' && s[start-1] <= '9' {
		start--
	}
	if start == end {
		return 0
	}
	var n int64
	for i := start; i < end; i++ {
		n = n*10 + int64(s[i]-'0')
	}
	return n
}

// rescanLog is the in-memory store of last-rescan timestamps per
// folder path. Implements both reconcile.LastRescanRecorder (called
// by the applier on successful rescan) and reconcile.LastRescanQuerier
// (called by the provider when populating Observed). Resets on
// daemon restart — the cost is one extra rescan per folder after
// each restart, which is benign.
type rescanLog struct {
	mu      sync.RWMutex
	entries map[string]time.Time
}

func newRescanLog() *rescanLog {
	return &rescanLog{entries: make(map[string]time.Time)}
}

// seedRescanLog populates the log with time.Now() for every
// currently-managed folder root. Called once at daemon startup so
// the first reconcile sees "just rescanned" rather than "never
// rescanned." Without this seeding, v0.9.7's backstop logic fires
// a RescanFolderNow for every folder within seconds of daemon
// start — on a multi-folder fleet, that burst can overwhelm
// Syncthing's REST endpoint (one folder timed out at the default
// 5s when 22 rescans hit at once during the v0.9.7 deploy).
//
// Side-effect: a fresh daemon defers all backstop rescans by one
// backstop interval (7 days for reliable filesystems, 24h for
// unreliable). For the daemon-restart case that's correct: we
// don't actually need to rescan; nothing meaningful has happened.
// For the first-ever boot case there's a small theoretical window
// where Syncthing's own scan history is empty too, but Syncthing
// always scans at startup independent of dotkeeper, so the bases
// are covered.
func (r *rescanLog) seed(paths []string, at time.Time) {
	r.mu.Lock()
	defer r.mu.Unlock()
	for _, p := range paths {
		// Don't overwrite an existing entry — the seed represents
		// a baseline, not a forced reset. (Currently the only call
		// site is daemon startup where the map is freshly empty,
		// but the guard is cheap and future-proofs against e.g.
		// re-seeding on folder-set change.)
		if _, ok := r.entries[p]; !ok {
			r.entries[p] = at
		}
	}
}

func (r *rescanLog) RecordRescan(path string, at time.Time) {
	r.mu.Lock()
	r.entries[path] = at
	r.mu.Unlock()
}

func (r *rescanLog) LastRescan(path string) (time.Time, bool) {
	r.mu.RLock()
	defer r.mu.RUnlock()
	v, ok := r.entries[path]
	return v, ok
}

// startActivityTracker creates an activity.Tracker over every
// currently-managed folder root. Returns nil (auto-pause disabled)
// when there are no managed folders or the tracker can't start —
// both are non-fatal because the daemon must keep running.
//
// Folders added or removed *after* startup are not reflected in the
// tracker's watch set in this v0.9.6 first cut. The trade-off:
// folders added after startup don't auto-pause until the next daemon
// restart; folders removed after startup have their watches dangle
// until the same. Both are recoverable on the next service restart.
// A proper "reload roots on reconcile-detected folder set change" is
// possible but adds enough complexity that it's worth waiting for a
// reported user impact before shipping it.
func startActivityTracker(ctx context.Context, logger *slog.Logger) *activity.Tracker {
	roots := managedFolderPathsV5()
	if len(roots) == 0 {
		logger.InfoContext(ctx, "activity tracker disabled (no managed folders yet)")
		return nil
	}
	tr, err := activity.New(roots)
	if err != nil {
		logger.WarnContext(ctx, "activity tracker failed to start; auto-pause disabled", "err", err)
		return nil
	}
	logger.InfoContext(ctx, "activity tracker started", "roots", len(roots))
	return tr
}

// bareInitCmd configures peer-side git repositories so that
// dotkeeper's GitSSHTransport can push directly to them. The peer
// side runs `git config receive.denyCurrentBranch updateInstead`
// in every tracked folder — that tells git to accept pushes to
// the currently-checked-out branch AND atomically update the
// working tree on each push.
//
// Why this design (vs the more conventional bare-repo +
// post-receive hook): one configuration knob per folder instead
// of provisioning a bare repo + installing hooks, no second
// authoritative copy of the data on the peer, no path-mapping
// problem (peer's working tree lives at the same path as the
// local one — already a dotkeeper invariant via `scan_roots`),
// and natural coexistence with Syncthing (both transports write
// to the same working tree; identical content is a no-op for
// either).
//
// Prerequisites the operator must satisfy:
//   - SSH key access from this host to the peer's account
//     (authorized_keys configured ahead of time)
//   - Same folder path on the peer as on this host (the v1.0.0
//     constraint; a per-peer path map is a planned extension)
//   - Each tracked folder exists as a git working tree on the
//     peer (dotkeeper-managed folders are git working trees by
//     definition)
func bareInitCmd() *cobra.Command {
	var peerName string
	var hostOverride string
	cmd := &cobra.Command{
		Use:   "bare-init [--peer=NAME] [--host=USER@ADDR]",
		Short: "Configure peer-side git repos for direct-push transport",
		Long: "For each folder tracked by dotkeeper on this host, SSH to\n" +
			"the named peer (or every peer in machine.toml if --peer is\n" +
			"omitted) and run:\n\n" +
			"  git -C <folder> config receive.denyCurrentBranch updateInstead\n\n" +
			"This enables `git push` directly to the peer's working tree —\n" +
			"the basis of dotkeeper's v1.0.0 GitSSHTransport. Idempotent:\n" +
			"re-running on an already-configured folder is a no-op.\n\n" +
			"Peer address resolution order:\n" +
			"  1. --host=USER@ADDR (operator override)\n" +
			"  2. Any registered resolver that knows the peer\n" +
			"     (v1.0.0 ships a Tailscale resolver; mDNS and\n" +
			"     static-hub variants are planned)\n" +
			"  3. The peer.Name itself (assume it's resolvable as a\n" +
			"     hostname via the operator's /etc/hosts, DNS, or\n" +
			"     SSH config)\n\n" +
			"Prerequisites: SSH key access already configured to the\n" +
			"peer (`ssh peer-address true` must succeed); each folder\n" +
			"exists at the same absolute path on both hosts (the v1.0.0\n" +
			"path-mirroring constraint).",
		RunE: func(cmd *cobra.Command, args []string) error {
			machine, err := config.LoadMachineConfigV2()
			if err != nil || machine == nil {
				return fmt.Errorf("load machine config: %w", err)
			}

			peers := machine.Peers
			if peerName != "" {
				filtered := peers[:0]
				for _, p := range peers {
					if p.Name == peerName {
						filtered = append(filtered, p)
					}
				}
				if len(filtered) == 0 {
					return fmt.Errorf("peer %q not found in machine config", peerName)
				}
				peers = filtered
			}
			if len(peers) == 0 {
				return fmt.Errorf("no peers configured; pair a peer first with `dotkeeper peer add`")
			}

			folders := managedFolderPathsV5()
			if len(folders) == 0 {
				return fmt.Errorf("no managed folders; track at least one before bare-init")
			}

			// Construct the list of available resolvers in
			// preference order. v1.0.0 ships one resolver
			// (Tailscale); mDNS and static-hub variants are
			// planned extensions. Each resolver's Available() is
			// consulted lazily inside resolveBareInitAddress so an
			// unconfigured resolver (e.g. the resolver's
			// prerequisite tool isn't installed) simply
			// contributes nothing.
			resolvers := []transport.Resolver{
				transport.NewTailscaleResolver(),
			}

			if hostOverride != "" && len(peers) > 1 {
				return fmt.Errorf("--host can only be used together with --peer (a single override address can't apply to every peer)")
			}

			ctx := cmd.Context()
			out := cmd.OutOrStdout()
			anyErr := false
			for _, p := range peers {
				addr, source, err := resolveBareInitAddress(ctx, p, hostOverride, resolvers)
				if err != nil {
					_, _ = fmt.Fprintf(out, "peer %q: skip (%v)\n", p.Name, err)
					anyErr = true
					continue
				}
				for _, folder := range folders {
					_, _ = fmt.Fprintf(out, "peer %q (%s via %s) folder %q: configuring receive.denyCurrentBranch=updateInstead\n",
						p.Name, addr, source, folder)
					output, err := bareInitSSHRunner(ctx, addr,
						"git", "-C", shellQuote(folder),
						"config", "receive.denyCurrentBranch", "updateInstead")
					if err != nil {
						_, _ = fmt.Fprintf(out, "  FAILED: %v: %s\n", err, strings.TrimSpace(string(output)))
						anyErr = true
						continue
					}
					_, _ = fmt.Fprintf(out, "  ok\n")
				}
			}
			if anyErr {
				return fmt.Errorf("bare-init completed with errors; see output above")
			}
			return nil
		},
	}
	cmd.Flags().StringVar(&peerName, "peer", "", "configure only this peer (default: every peer)")
	cmd.Flags().StringVar(&hostOverride, "host", "", "SSH target address (USER@ADDR or just ADDR); overrides all resolvers")
	return cmd
}

// bareInitSSHRunner is the function bareInitCmd uses to execute
// the remote `git config` command via ssh. Package-level var so
// tests can swap in a stub returning controlled output and errors.
// The default implementation forks a real `ssh` binary; production
// callers never override it.
var bareInitSSHRunner = func(ctx context.Context, target string, args ...string) ([]byte, error) {
	cmd := exec.CommandContext(ctx, "ssh",
		append([]string{
			"-o", "BatchMode=yes",
			"-o", "ConnectTimeout=5",
			"-o", "StrictHostKeyChecking=accept-new",
			target,
		}, args...)...)
	return cmd.CombinedOutput()
}

// resolveBareInitAddress walks the address-resolution ladder
// described in bareInitCmd's --help: explicit override → any
// available resolver → peer.Name as a last-resort hostname.
// Returns the resolved address and a short label for the source
// (used in operator-facing log lines). The "name as hostname"
// fallback exists so users who have configured their SSH client
// (via /etc/hosts, ~/.ssh/config, or local DNS) can use the peer's
// dotkeeper name as the SSH target without needing a configured
// resolver inside dotkeeper itself.
func resolveBareInitAddress(ctx context.Context, p config.PeerEntry, override string, resolvers []transport.Resolver) (addr, source string, err error) {
	if override != "" {
		return override, "override", nil
	}
	peer := transport.Peer{Name: p.Name, DeviceID: p.DeviceID, Hostname: p.Name}
	for _, r := range resolvers {
		if !r.Available() {
			continue
		}
		a, rerr := r.Resolve(ctx, peer)
		if rerr == nil {
			return a, r.Name(), nil
		}
		// ErrPeerUnknown is the expected "this resolver doesn't
		// know about this peer" signal; we move on to the next
		// resolver. Other errors (ErrResolverUnavailable, etc.)
		// also fall through; a future revision may log differently
		// to distinguish "resolver broken" from "resolver doesn't
		// know peer."
		_ = rerr
	}
	// Last-resort fallback: assume the peer name itself is an
	// SSH-reachable hostname. Works for fleets with /etc/hosts
	// entries, ~/.ssh/config Host blocks, or LAN-local DNS.
	return p.Name, "name-as-hostname", nil
}

// shellQuote returns s wrapped in single quotes with embedded
// single-quotes escaped. We use it for paths passed to remote
// shells via ssh, where the path becomes one argument to remote
// `sh -c "..."` after SSH's own argument concatenation. Without
// quoting, a path with spaces (like macOS's "/Users/me/My
// Documents") would split across multiple remote arguments.
func shellQuote(s string) string {
	return "'" + strings.ReplaceAll(s, "'", `'\''`) + "'"
}

